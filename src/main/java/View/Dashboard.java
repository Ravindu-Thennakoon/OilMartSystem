private void loadTableDataAsync() {
    SwingWorker<List<Transaction>, Void> worker = new SwingWorker<>() {
        @Override
        protected List<Transaction> doInBackground() throws Exception {
            // Background thread: fetch items from DB
            return DashboardController.getAllItems();
        }

        @Override
        protected void done() {
            try {
                List<Transaction> transactions = get();

                // Define table columns
                String[] columnNames = {
                    "date_time", "cashier_id", "total_amount", "discount"
                };

                // Create a read-only table model
                DefaultTableModel model = new DefaultTableModel(columnNames, 0) {
                    @Override
                    public boolean isCellEditable(int row, int column) {
                        return false; // all cells read-only
                    }
                };

                // Fill rows
                for (Transaction transaction : transactions) {
                    Object[] row = new Object[] {
                        transaction.getDate(),
                        transaction.getCashierId(),
                        transaction.getTotalAmount(),
                        transaction.getDiscount()
                    };
                    model.addRow(row);
                }

                // Set the model to your JTable
                tblsale.setModel(model);

            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Failed to load data: " + e.getMessage());
            }
        }
    };

    worker.execute();
}


    
